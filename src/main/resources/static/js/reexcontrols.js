// JavaScript source code
function showFormContatoCompra(){
			var x = document.getElementById("form-contato-compra");			
			if (x.style.display == "none"){
				x.style.display = "block";
				window.location.href = "#form-comprar";
			}else{
				x.style.display = "none";
			}
		}

